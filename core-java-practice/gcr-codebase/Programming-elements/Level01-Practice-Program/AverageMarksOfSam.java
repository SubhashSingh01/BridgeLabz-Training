public class AverageMarksOfSam {
    public static void main(String[] args) {
        int maths = 85; // Marks of maths
        int physics= 90; // Marks of physics
        int chemistry = 78; // Marks of chemistry
        int totalMarks = maths + physics + chemistry; // Calculate total marks
        double averageMarks = totalMarks / 3.0; // Calculate average marks
        System.out.println("Sam's average marks are: " + averageMarks);
    }
}
